// ERROR HANDLING - EXPRESSING 4 PARAMETERS MAKES EXPRESS RECOGNIZE THIS AS AN ERROR HANDLER
module.exports = (err, req, res, next) => {
  err.statusCode = err.statusCode || 500;
  err.status = err.status || 'error';

  res.status(err.statusCode).json({
    status: err.status,
    message: err.message,
  });
};
